def rgb(r:int,g:int,b:int) -> str:
    """
    Generate a color-string from r,g,b-values
    :param r: red
    :param g: green
    :param b: blue
    :return: Finished color-string
    """

    return f"#{r:#05x}{g:#05x}{b:#05x}".replace("0x","")

